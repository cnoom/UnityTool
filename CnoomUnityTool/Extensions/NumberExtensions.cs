using UnityEngine;

namespace CnoomUnityTool.Extensions
{
    public static class NumberExtensions
    {
        
        public static float Abs(this float f)
        {
            return Mathf.Abs(f);
        }
        
        public static int Abs(this int f)
        {
            return Mathf.Abs(f);
        }
        
        
    }
}